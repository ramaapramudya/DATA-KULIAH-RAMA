#include <iostream>
using namespace std;

int main()
{
    // membuat array kosong
    int nilai[5];

    // mengisi array
    nilai[0] = 10;
    nilai[1] = 20;
    nilai[2] = 30;
    nilai[3] = 40;
    nilai[4] = 50;

    // mencetak isi array
    cout << "Nilai ke-1 : " << nilai[0] << endl;
    cout << "Nilai ke-2 : " << nilai[1] << endl;
    cout << "Nilai ke-3 : " << nilai[2] << endl;
    cout << "Nilai ke-4 : " << nilai[3] << endl;
    cout << "Nilai ke-5 : " << nilai[4] << endl;

    return 0;
}