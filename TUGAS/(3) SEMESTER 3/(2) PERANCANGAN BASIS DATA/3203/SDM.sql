CREATE TABLE CATEGORY_PROD(
ID_CATEGORY NUMBER GENERATED BY DEFAULT AS IDENTITY,
CATEGORY_NAME VARCHAR2(50) NOT NULL,
PRIMARY KEY(ID_CATEGORY)
);


CREATE TABLE PRODUCT(
ID_PRODUCT NUMBER GENERATED BY DEFAULT AS IDENTITY,
PRODUCT_NAME VARCHAR2(50) NOT NULL,
PRICE DECIMAL(12,2) NOT NULL,
CATEGORY_ID INTEGER,
PRIMARY KEY(ID_PRODUCT),
CONSTRAINT FK_CategoryProduct FOREIGN KEY
(CATEGORY_ID) REFERENCES CATEGORY_PROD(ID_CATEGORY)
);


